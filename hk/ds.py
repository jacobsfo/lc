def superDigit(n, k):
    # Write your code here
    sum_n=0
    #intial sum
    for x in n:
        sum_n+=int(x)
    n=sum_n
    #recursive sum
    mod=((n%9)*(k%9)) % 9
    if mod==0:
        mod=9
    return mod

